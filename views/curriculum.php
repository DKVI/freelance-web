                                    <h2>Willem C. Vis International Commercial Arbitration Moot – Vis Moot (“Vis Moot”)</h2>
                                    <p class="vn">Vis Moot là sân chơi quốc tế dành cho các mooter có đam mê nghiên cứu về tranh chấp thương mại quốc tế. Các vấn đề trong Vis Moot thường bao gồm các vấn đề pháp lý và thực tiễn liên quan đến hợp đồng mua bán hàng hóa quốc tế được giải quyết theo quy định và nguyên tắc quốc tế như Công ước Liên Hợp Quốc về Hợp đồng mua bán hàng hóa quốc tế (“CISG”), Luật Mẫu của Ủy ban Liên hợp quốc về Luật Thương mại Quốc tế (“UNCITRAL”). Kể từ năm 1994, Willem C. Vis Moot được tổ chức thường niên tại Vienna, Áo, và thu hút sự tham gia của hơn 300 trường luật trên khắp thế giới. </p>
